package algorithms

// FindSecondMax находит второй по величине элемент в массиве
// Алгоритм:
// 1. Инициализируем два переменные для хранения наибольшего и второго наибольшего значений
// 2. Проходим по массиву, обновляя значения при нахождении большего элемента
// 3. Возвращаем второй по величине элемент
// Сложность: O(n) - один проход по массиву
func FindSecondMax(arr []int) int {
	if len(arr) < 2 {
		return -1
	}

	max, secondMax := arr[0], -1

	for i := 1; i < len(arr); i++ {
		if arr[i] > max {
			secondMax = max
			max = arr[i]
		} else if arr[i] < max && (arr[i] > secondMax || secondMax == -1) {
			secondMax = arr[i]
		}
	}

	return secondMax
}
